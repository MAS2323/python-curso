# Este es el modulo principal
def sumar(op1, op2):
    print("El resultado de la suma es: ", op1 + op2)


def restar(op1, op2):
    print("El resultado de la resta es: ", op1 - op2)


def multiplicart(op1, op2):
    print("El resultado es la multiplicacion es: ", op1 * op2)
